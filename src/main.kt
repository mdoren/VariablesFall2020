fun main(){
    //Assign values (because they do not change) to each different type of variables we're demoing
    val myByte: Byte = 0
    val myShort: Short = 2
    val myInt: Int = 62
    val myLong: Long = 41000000000L
    val myFloat: Float = 3.141592653F
    val myDouble = 839.22
    val myBoolean= true
    val myChar: Char = 'L'
    //Print the values
    println("My byte: $myByte")
    println("My short: $myShort")
    println("My Int: $myInt")
    println("My Long: $myLong")
    println("My Float: $myFloat")
    println("My Double: $myDouble")
    println("My Boolean: $myBoolean")
    println("My Char: $myChar")
}